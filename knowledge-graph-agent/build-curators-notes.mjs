// Curator's Notes generator (spec §14).
//
// Reads proposals.json + longlist.json and synthesises a public-facing log
// of changes Lexi has made to the graph: promotions (PROMOTE_TO_GRAPH
// applied), demotions (DEMOTE_TO_LONGLIST applied), and definition
// refreshes (EDIT_GRAPH_DEF_SUBSTANTIVE applied). Writes to
// /curators-notes/curators-notes.json which the public page renders.
//
// Per spec §14: "Release notes for readers — what changed in the graph
// and the longlist, when, and why in one sentence. Voice is impartial
// curator." V1 covers graph changes; longlist additions/retirements are
// a v2 followup (higher volume, less narratively rich than graph changes).
//
// Run with: node build-curators-notes.mjs
//
// Wired into the daily lexi-run.yml workflow as a build step (parallel to
// build-almanac.mjs). Re-runs after every apply-proposals so newly-applied
// changes appear in the public log within a single deploy cycle.

import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { ACTIONS } from "./actions.mjs";
import { loadJson, loadGraphData, mergeNodes, ensureParent, writeJson } from "./state-io.mjs";

main().catch(err => {
  console.error(err.stack || err.message);
  process.exitCode = 1;
});

async function main() {
  const here = path.dirname(fileURLToPath(import.meta.url));
  const repoRoot = path.resolve(here, "..");
  const configModule = await import(pathToFileURL(path.resolve(here, "config.mjs")).href);
  const config = configModule.default;

  const proposals = await loadJson(config.proposalsPath, { proposals: [] });
  const longlist = await loadJson(config.longlistPath, { entries: [] });
  const trusted = await loadJson(config.trustedSourcesPath, { sources: [] });
  // Graph + overlay needed for def-refresh proposals — their payload only
  // carries the new def, not the term label. Falling back to graph lookup
  // means GPT-5.4 reads as "GPT-5.4" rather than "gpt54" in the public log.
  const graph = await loadGraphData(config.graphDataPath);
  const patch = await loadJson(config.patchJsonPath, { nodes: [], definitionOverrides: [] });
  const mergedNodes = mergeNodes(graph.nodes, patch);
  const graphById = Object.fromEntries(mergedNodes.map(n => [n.id, n]));

  const longlistById = Object.fromEntries((longlist.entries || []).map(e => [e.id, e]));
  const trustedDomains = new Set((trusted.sources || []).map(s => (s.domain || "").toLowerCase()));

  // We only surface decided proposals — applied (graph mutated) or
  // rejected (Nicole's editorial verdict on record). Pending and approved-
  // but-not-yet-applied are work-in-progress, not "what changed."
  const decidable = (proposals.proposals || []).filter(p =>
    p.status === "applied" || p.status === "rejected"
  );

  const entries = decidable
    .map(p => buildEntry(p, longlistById, graphById, trustedDomains))
    .filter(Boolean)
    .sort((a, b) => (b.datetime || "").localeCompare(a.datetime || ""));

  const generatedAt = new Date().toISOString();
  const out = {
    meta: {
      generatedAt,
      totalEntries: entries.length,
      note: "Curator's Notes (spec §14): public-facing log of changes Lexi has made to the graph. Generated by knowledge-graph-agent/build-curators-notes.mjs from applied + rejected proposals."
    },
    entries
  };

  const outPath = path.resolve(repoRoot, "curators-notes/curators-notes.json");
  await ensureParent(outPath);
  await writeJson(outPath, out);
  console.log(`Curator's Notes written: ${outPath}`);
  console.log(`  Total entries: ${entries.length}`);
  const byAction = entries.reduce((a, e) => { a[e.action] = (a[e.action] || 0) + 1; return a; }, {});
  for (const [action, n] of Object.entries(byAction).sort()) console.log(`  ${action}: ${n}`);
}

function buildEntry(proposal, longlistById, graphById, trustedDomains) {
  const actionType = mapActionType(proposal);
  if (!actionType) return null;

  const targetId = proposal.target?.id;
  const llEntry = targetId ? longlistById[targetId] : null;
  const graphNode = targetId ? graphById[targetId] : null;
  // Resolve display label by priority: payload.graphNode.label (set by
  // promote.mjs's buildPromotionProposal), longlist label, current graph
  // label (covers def-refresh and demote cases where the term lives on
  // the graph but isn't on the longlist), demote.mjs payload.label, then
  // the bare id as last resort.
  const term = proposal.payload?.graphNode?.label
    || llEntry?.label
    || graphNode?.label
    || proposal.payload?.label
    || targetId;

  const datetime = proposal.appliedAt || proposal.rejectedAt || proposal.proposedAt;
  const date = (datetime || "").slice(0, 10);

  // Rationale: prefer the proposal's reason field (already a sentence),
  // fall back to a synthesized one if absent. For rejections, prefer the
  // rejectionReason if it's more specific.
  let rationale = proposal.reason || "";
  if (proposal.status === "rejected" && proposal.rejectionReason) {
    rationale = proposal.rejectionReason;
  }

  // Trusted-source annotation when the entry's sources include any from
  // the trusted list. Surfaces credibility texture on the public page
  // (matches the badge on /observing).
  const entrySources = llEntry?.sources || proposal.payload?.graphNode?.refs || [];
  const trustedHits = [...new Set(
    entrySources
      .map(s => (s.domain || "").toLowerCase())
      .filter(d => d && trustedDomains.has(d))
  )];

  return {
    id: proposal.id,
    action: actionType,
    term,
    termId: targetId,
    date,
    datetime,
    rationale,
    decidedBy: proposal.approvedBy || proposal.rejectedBy || "nicole",
    sourceCount: llEntry?.sourceCount ?? null,
    trustedSources: trustedHits.length > 0 ? trustedHits : null,
    // Graph link only meaningful for promotions / refreshes (term IS on
    // the graph). For demotions and rejections, the term isn't there;
    // the dashboard renderer can decide whether to render the link.
    graphLink: (actionType === "promoted" || actionType === "refreshed") && targetId
      ? `/#${targetId}`
      : null
  };
}

function mapActionType(proposal) {
  if (proposal.status === "rejected") return "rejected";
  if (proposal.status !== "applied") return null;
  switch (proposal.action) {
    case ACTIONS.PROMOTE_TO_GRAPH: return "promoted";
    case ACTIONS.DEMOTE_TO_LONGLIST: return "demoted";
    case ACTIONS.EDIT_GRAPH_DEF_SUBSTANTIVE: return "refreshed";
    case ACTIONS.DEPRECATE_GRAPH_NODE: return "deprecated";
    case ACTIONS.CONFLATE_GRAPH_NODES: return "conflated";
    case ACTIONS.SPLIT_GRAPH_NODE: return "split";
    default: return null;
  }
}
